"""
TTL механика для Spoon Messenger.
Временное окно зашивается в BF программу как обёртка.
После истечения TTL программа выводит мусор вместо текста.
"""

import time

# Точность TTL — округление до N секунд
# Отправитель и получатель должны быть синхронизированы
TTL_PRECISION = 60  # 1 минута


def get_time_token(timestamp: float = None, precision: int = TTL_PRECISION) -> int:
    """
    Получить временной токен — округлённое Unix время.
    Одинаковый у отправителя и получателя в пределах precision секунд.

    Вход:  timestamp = None (использовать текущее время)
    Выход: int — временной токен
    """
    if timestamp is None:
        timestamp = time.time()
    return int(timestamp) // precision


def wrap_with_ttl(bf_code: str, ttl_seconds: int = 3600) -> str:
    """
    Обернуть BF программу в TTL обёртку.

    Логика:
    - Вычислить ожидаемый временной токен (текущее время + ttl_seconds) // precision
    - Зашить его в BF как константу
    - При выполнении программа проверяет системное время через stdin
    - Если токен совпадает — выполняет оригинальный код
    - Если не совпадает — выводит мусор

    Вход:  bf_code    — оригинальная BF программа
           ttl_seconds — время жизни в секундах
    Выход: BF программа с TTL обёрткой
    """
    current_time = time.time()
    # токен действителен от сейчас до сейчас + ttl_seconds
    valid_token = get_time_token(current_time)
    expires_token = get_time_token(current_time + ttl_seconds)

    # Зашиваем valid_token как BF константу
    # Программа при запуске получает текущий токен через stdin
    # и сравнивает с зашитым значением
    #
    # Структура обёртки:
    # 1. Записать ожидаемый токен в ячейку 0 (expires_token % 256)
    # 2. Прочитать фактический токен из stdin в ячейку 1
    # 3. Сравнить — если равны, выполнить оригинальный код
    # 4. Если не равны — вывести мусор (случайные символы)

    token_val = expires_token % 256  # токен как байт

    # BF код для записи токена в ячейку 0
    set_token = '+' * token_val

    # Обёртка:
    # ячейка 0: ожидаемый токен
    # ячейка 1: полученный токен из stdin
    # ячейка 2: рабочая область для сравнения
    wrapper = (
        # установить ожидаемый токен в ячейку 0
        set_token +
        # перейти в ячейку 1, прочитать входной токен
        '>,' +
        # скопировать ячейку 1 в ячейку 2 для сравнения
        '[->+>+<<]' +   # cell1=0, cell2=copy, cell3=copy
        '>>[-<<->>]' +  # вычесть cell0 из cell2 (cell2 = input - expected)
        # если cell2 == 0 (токены совпали) — выполнить оригинальный код
        # перейти в ячейку 2
        '<<' +
        # если ячейка 0 не ноль после вычитания — мусор
        # иначе — оригинальный код
        '>>' +
        '[' +
            # токены не совпали — вывести мусор
            '++++++++.' * 8 +
        ']' +
        # сброс и выполнение оригинального кода
        # переходим к оригинальному коду
        '>>>>' +
        bf_code
    )

    return wrapper


def wrap_simple(bf_code: str, ttl_seconds: int = 3600) -> tuple:
    """
    Упрощённая TTL обёртка — надёжная и тестируемая.

    Возвращает (bf_with_ttl, expires_at_timestamp)

    Логика:
    - expires_token зашит в программу
    - при запуске передаём current_token через stdin
    - программа сравнивает и выполняет или молчит
    """
    current_time = time.time()
    expires_at = int(current_time + ttl_seconds)
    # Зашиваем ТЕКУЩИЙ токен: сообщение действительно пока
    # текущее время находится в том же TTL_PRECISION-окне что и при отправке.
    # expires_at — метаданные для отображения срока жизни.
    current_token = get_time_token(current_time)
    expected = current_token % 256

    ttl_wrapper = (
        # cell0 = expected token
        '+' * expected +
        # cell1 = input token
        '>,' +
        # cell1 -= cell0 (сравнение)
        '<[->-<]>' +
        # если cell1 == 0 → выполнить оригинальный код
        # [если не 0 → пропустить]
        # BF не имеет if/else напрямую, используем трюк:
        # инвертировать cell1: если 0 → стало 1, если не 0 → обнулить
        # затем использовать как флаг
        # Трюк: cell2 = 1, cell1[cell2=0], если cell2==1 → токен совпал
        '>+' +           # cell2 = 1
        '<[>-<[-]]>' +   # если cell1 != 0: cell2=0, cell1=0
        # теперь cell2: 1=совпало, 0=не совпало
        '[' +            # если совпало (cell2==1)
            '[-]' +      # обнулить cell2
            '>' +        # перейти к рабочей области
            bf_code +    # выполнить оригинальный код
        ']'
    )

    return ttl_wrapper, expires_at


def get_stdin_token(timestamp: float = None) -> str:
    """
    Получить токен для передачи в stdin BF программы.
    Возвращает символ с кодом = token % 256

    Вход:  timestamp = None (использовать текущее время)
    Выход: строка из одного символа для передачи как stdin
    """
    token = get_time_token(timestamp)
    return chr(token % 256)
